import java.util.*;

public class Main {

    public static void main(String[] args) {
        Scanner scan = new Scanner(System.in);

        int a = scan.nextInt();
        int i = 0;

        for (i = 1; i <= 10; i++) {
            System.out.println(i + " x " + a + " = " + i * a);


        }

    }
}
